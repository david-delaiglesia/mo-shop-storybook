export const mockSearch = (params) => {
  let mockProduct = [...products]
  const query = params.query

  if (!query) {
    return Promise.resolve({ query: '', hits: mockProduct })
  }

  if (params.filters && params.filters.length) {
    mockProduct = mockProduct.filter((item) =>
      params.filters.includes(item.brand),
    )
  }

  if (params.facetFilters && params.facetFilters.length) {
    mockProduct = mockProduct.filter((item) =>
      params.facetFilters[0].includes(item.categories[0].id),
    )
  }

  if (query === 'jamones') {
    mockProduct = mockProduct.slice(-2)
  }
  if (query === 'turia') {
    mockProduct = [turiaProduct]
  }

  if (query === 'empty') {
    mockProduct = []
  }

  return Promise.resolve({
    query,
    hits: mockProduct,
    queryID: 'b25283e88b8a64a9eaf985ad03ffcd8b',
  })
}

